# This code prints, adds and multiplies two user input numbers #
# Week 2, Exercise code #

a = float(input("Please enter a number: "))
b = float(input("Please enter another number: "))

print(f"The value of 'a' was {a} and the value of 'b' was {b}")

sum = a + b
print(f"The sum of 'a' and 'b' is {sum}")

prod = a * b
print(f"The product of 'a' and 'b' is {prod}")
